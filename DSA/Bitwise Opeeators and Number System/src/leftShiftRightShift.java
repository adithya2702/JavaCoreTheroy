public class leftShiftRightShift {
    public static void main(String[] args) {
        //LeftShift
        System.out.println(10<<1);//10*2
        System.out.println(20<<2);//20*4
        //Basically here it doubles number as a shift in binary place.
        //a<<b= a*(2^b);

        //Right shift
        System.out.println(10>>1);//    10/2
        System.out.println(20>>2);//    20/4
        //Basically here it divides number by 2 as a shift in binary place.
        //a<<b= a/(2^b);
    }
}
